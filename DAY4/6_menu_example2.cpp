#include <iostream>
#include <string>
#include <vector>
#include <conio.h>

// 객체지향 프로그램의 핵심
// => 필요한 타입을 먼저 설계 하라

class MenuItem
{
	std::string title;
	int id;
public:
	MenuItem(const std::string& title, int id) 
		: title{ title }, id{ id } {}

	std::string get_title() const { return title; }

	void command()
	{
		std::cout << get_title() << "메뉴 선택됨\n";

		_getch(); // 아무 키나 누를때를 잠시 대기 
	}
};

int main()
{
	MenuItem m1("김밥", 11);
	MenuItem m2("라면", 12);

	m1.command(); // 메뉴 선택!
}
